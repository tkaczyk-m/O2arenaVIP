const translations = { cs, en }

/**
 * Get a nested translation value using dot notation.
 * Supports function values (called with args).
 */
export function getT(locale, key, ...args) {
  const keys = key.split('.')
  let value = translations[locale] || translations.cs
  for (const k of keys) {
    if (value === undefined) return key
    value = value[k]
  }
  if (typeof value === 'function') return value(...args)
  if (typeof value === 'string') return value
  return key
}
